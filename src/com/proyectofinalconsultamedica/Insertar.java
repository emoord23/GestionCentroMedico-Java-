    /**
     * Método que genera un log en un fichero tipo *.txt guardado en la raíz del 
     * proyecto. 
     * @throws IOException 
     */
    public void controlRegistros() throws IOException {
        File controlInserciones = new File("logRegistros.txt");
        FileWriter fic = new FileWriter(controlInserciones, true);
        String cadena = "Registro grabado correctamente\n";
        char[] cad = cadena.toCharArray();
        for (int j = 0; j < cad.length; j++) {
            fic.write(cad[j]);
        }
        fic.write("\r\n");
        fic.close();
    }
    /**
     * Método que recibe los parámetros String correspondientes a todos las columnas de la tabla pacientes
     * de la BBDD. Realiza la inserción de dichas variables mediante la sentencia SQL INSERT
     * @param nombre String
     * @param apellidos String
     * @param dni String
     * @param direccion String
     * @param telefono String
     * @param dolencia String
     * @throws ClassNotFoundException
     * @throws IOException 
     */
    public void insertar(String nombre, String apellidos, String dni, String direccion, String telefono, String dolencia) throws ClassNotFoundException, IOException {
        
            try {
                Class.forName("org.sqlite.JDBC");
                Connection conn = DriverManager.getConnection("jdbc:sqlite:ConsultaMedica.sqlite");
                Statement stat = conn.createStatement();
                stat.executeUpdate("INSERT INTO pacientes(nombre, apellidos, dni, direccion, telefono, dolencia) VALUES "
                        + "('" + nombre + "','" + apellidos + "','" + dni + "','" + direccion + "','" + telefono + "','" + dolencia + "')");
                controlRegistros();
                txtComprobar.setBackground(Color.green);
                txtComprobar.setText("Paciente añadido correctamente");
                stat.close();
                conn.close();
            } catch (SQLException ex) {
                txtComprobar.setBackground(Color.red);
                txtComprobar.setText("Error al insertar");
                System.out.println("Error");
            }
       
    }

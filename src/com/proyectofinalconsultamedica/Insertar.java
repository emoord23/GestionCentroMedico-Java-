    /**
     * Evento que ejecuta el método insertar() al hacer click sobre el botón Insertar. Pasándole como
     * parámetros todos los valores recogidos en los campos de texto correpondientes. No permite campos vaciós,
     * mostrando un error en un txt si se introduce alguno vacío.
     * @param evt 
     */
    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed

        String nombre = txtNombre.getText();
        String apellidos = txtApellidos.getText();
        String dni = txtDni.getText();
        String direccion = txtDireccion.getText();
        String telefono = txtTelefono.getText();
        String dolencia = txtDolencia.getText();
        //comprobación de que no existan campos txt vacios
        if(!nombre.isEmpty() & !apellidos.isEmpty() & !dni.isEmpty() 
                & !direccion.isEmpty() & !telefono.isEmpty() & !dolencia.isEmpty() ){
        try {
            insertar(nombre, apellidos, dni, direccion, telefono, dolencia);
            txtNombre.setText("");
            txtApellidos.setText("");
            txtDni.setText("");
            txtDireccion.setText("");
            txtTelefono.setText("");
            txtDolencia.setText("");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Insertar.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Insertar.class.getName()).log(Level.SEVERE, null, ex);
        }

        } else 
         {
         txtComprobar.setBackground(Color.red);
         txtComprobar.setText("Ningun campo puede estar vacío");
         }

    }//GEN-LAST:event_btnInsertarActionPerformed

